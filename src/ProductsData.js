// CALCULATE SPAN FLASHSALE WIDTH
export function calcFlashSpan(flashSpan){
    let itemsR = '';
    let resSpan = '';
    flashSpan.map((product)=>{
        itemsR = Number(product.itemsRemaining);
        resSpan = ((itemsR*100)/20).toFixed(0);
        product.span = resSpan;
    });
     
}

window.addEventListener('DOMContentLoaded',calcFlashSpan(FlashSaleProducts));


