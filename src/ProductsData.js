// CALCULATE DISCOUNT
function calclDiscount(products){
    let currentPrice = '';
    let initialPrice = '';
    let result = '';
    products.map((product)=>{
    currentPrice = Number(product.currentPrice);
    initialPrice = Number(product.initialPrice); 
    
    result = (((initialPrice - currentPrice)*100)/initialPrice).toFixed(0);
    product.percentDiscount = `${result}%`;
})
}

